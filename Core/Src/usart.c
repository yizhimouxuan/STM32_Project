void LIDAR_UART_Start(void)
{
  HAL_StatusTypeDef st = HAL_UARTEx_ReceiveToIdle_DMA(&huart3, lidar_rx_buf, LIDAR_RX_BUF_LEN);
  if (st == HAL_OK)
  {
    __HAL_DMA_DISABLE_IT(&hdma_usart3_rx, DMA_IT_HT);
    return;
  }

  /* RX DMA is already running, keep it as-is and avoid disruptive abort. */
  if (st == HAL_BUSY && huart3.RxState == HAL_UART_STATE_BUSY_RX)
  {
    return;
  }

  LIDAR_UART_ForceRestart();
}

void LIDAR_UART_ForceRestart(void)
{
  HAL_StatusTypeDef st;

  (void)HAL_UART_DMAStop(&huart3);
  (void)HAL_UART_AbortReceive(&huart3);
  __HAL_UART_CLEAR_OREFLAG(&huart3);
  __HAL_UART_CLEAR_FEFLAG(&huart3);
  __HAL_UART_CLEAR_NEFLAG(&huart3);
  __HAL_UART_CLEAR_PEFLAG(&huart3);

  st = HAL_UARTEx_ReceiveToIdle_DMA(&huart3, lidar_rx_buf, LIDAR_RX_BUF_LEN);
  if (st == HAL_OK)
  {
    __HAL_DMA_DISABLE_IT(&hdma_usart3_rx, DMA_IT_HT);
  }
}

void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size)
{
  if (huart == &huart3)
  {
    lidar_rx_size = Size;
    lidar_rx_count++;
    Lidar_ProcessFrame(lidar_rx_buf, lidar_rx_size);
    LIDAR_UART_Start();
  }
}

void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart)
{
  if (huart == &huart3)
  {
    lidar_rx_error_count++;
    __HAL_UART_CLEAR_OREFLAG(&huart3);
    __HAL_UART_CLEAR_FEFLAG(&huart3);
    __HAL_UART_CLEAR_NEFLAG(&huart3);
    __HAL_UART_CLEAR_PEFLAG(&huart3);
    LIDAR_UART_Start();
  }
}

